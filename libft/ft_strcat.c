#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int idest;
	int isrc;

	idest = 0;
	isrc = 0;
	while (dest[idest] != '\0')
		idest++;
	while (src[isrc] != '\0')
	{
		dest[idest] = src[isrc];
		idest++;
		isrc++;
	}
	dest[idest] = '\0';
	return (dest);
}
